package ru.job4j.inheritance;

public class Profession {
    private String name;
    private int id;
    private String education;
    private  int birthday;

    public String getName() {
        return name;
    }

    public String getEducation() {
        return education;
    }

    public int getBirthday() {
        return birthday;
    }

    public int getId() {
        return id;
    }
}
